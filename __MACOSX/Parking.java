package kz.attractor.java.__MACOSX;

public class Parking {
    private int time;
    private double money;

    public Parking(int time, double money){
        this.time = time;
        this.money = money;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public double getMoney() {
        return money;
    }

    public void setMoney(double money) {
        this.money = money;
    }
}
